// sum /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function sum(arr) {
//edge: if array is empty return 0
var output = 0;
if(!arr.length){
  return 0;
}
for(let i = 0; i < arr.length; i++){
  output += arr[i];
}
return output;
}

