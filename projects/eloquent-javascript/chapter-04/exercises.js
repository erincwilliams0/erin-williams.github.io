////////////////////////////////////////////////////////////////////////////////

function reverseArray(arr) {
// loop through array from end to start pushing elements into an output array each iteration
var output = [];
for(let i = arr.length - 1; i >= 0; i--){
  output.push(arr[i]);
}
return output;
}

